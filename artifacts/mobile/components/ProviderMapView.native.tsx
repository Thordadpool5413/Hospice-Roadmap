function getPinColor(qualitySummary?: QualitySummary | null, rating?: number): string {
  const star = qualitySummary?.starRating ? parseFloat(qualitySummary.starRating) : null;
  const hci = qualitySummary?.hciScore ? parseFloat(qualitySummary.hciScore) : null;

  if (star !== null && !isNaN(star)) {
    if (star >= 4) return "#34C759";
    if (star >= 2.5) return "#D59A32";
    return "#8F9AB8";
  }
  if (hci !== null && !isNaN(hci)) {
    if (hci >= 7) return "#34C759";
    if (hci >= 4) return "#D59A32";
    return "#8F9AB8";
  }
  if (rating) {
    if (rating >= 4) return "#34C759";
    if (rating >= 2.5) return "#D59A32";
    return "#8F9AB8";
  }
  return "#8F9AB8";
}

function getRegionForProviders(providers: ProviderWithCoords[]): Region {
  if (providers.length === 0) {
    return { latitude: 39.5, longitude: -98.35, latitudeDelta: 20, longitudeDelta: 20 };
  }
  const lats = providers.map((p) => p.latitude);
  const lngs = providers.map((p) => p.longitude);
  const minLat = Math.min(...lats);
  const maxLat = Math.max(...lats);
  const minLng = Math.min(...lngs);
  const maxLng = Math.max(...lngs);
  const latDelta = Math.max((maxLat - minLat) * 1.4, 0.05);
  const lngDelta = Math.max((maxLng - minLng) * 1.4, 0.05);
  return {
    latitude: (minLat + maxLat) / 2,
    longitude: (minLng + maxLng) / 2,
    latitudeDelta: latDelta,
    longitudeDelta: lngDelta,
  };
}

export interface ProviderWithCoords extends Provider {
  latitude: number;
  longitude: number;
}

interface ProviderMapViewProps {
  providers: Provider[];
  qualitySummaries: Record<string, QualitySummary>;
  topInset: number;
}

const { height: SCREEN_HEIGHT } = Dimensions.get("window");
const SHEET_HEIGHT = 220;

export function ProviderMapView({ providers, qualitySummaries, topInset }: ProviderMapViewProps) {
  const mapRef = useRef<MapView>(null);
  const sheetAnim = useRef(new Animated.Value(0)).current;
  const [selectedProvider, setSelectedProvider] = useState<ProviderWithCoords | null>(null);

  const providersWithCoords = useMemo<ProviderWithCoords[]>(() => {
    return providers
      .map((p) => {
        // Prefer server-geocoded coordinates (exact street address).
        if (typeof p.latitude === "number" && typeof p.longitude === "number") {
          return { ...p, latitude: p.latitude, longitude: p.longitude };
        }
        // Fallback: ZIP-prefix centroid when geocoding was unavailable.
        const fallback = ZIP_COORDS[p.zip?.slice(0, 3) ?? ""];
        if (!fallback) return null;
        return { ...p, latitude: fallback[0], longitude: fallback[1] };
      })
      .filter((p): p is ProviderWithCoords => p !== null);
  }, [providers]);

  const initialRegion = useMemo(
    () => getRegionForProviders(providersWithCoords),
    [providersWithCoords]
  );

  const showSheet = useCallback((provider: ProviderWithCoords) => {
    setSelectedProvider(provider);
    Animated.spring(sheetAnim, {
      toValue: 1,
      useNativeDriver: true,
      tension: 60,
      friction: 10,
    }).start();
  }, [sheetAnim]);

  const hideSheet = useCallback(() => {
    Animated.timing(sheetAnim, {
      toValue: 0,
      duration: 200,
      useNativeDriver: true,
    }).start(() => setSelectedProvider(null));
  }, [sheetAnim]);

  const handleDirections = useCallback((provider: ProviderWithCoords) => {
    const address = encodeURIComponent(
      `${provider.address}, ${provider.city}, ${provider.state} ${provider.zip}`
    );
    const url = Platform.select({
      ios: `maps://maps.apple.com/?daddr=${address}`,
      android: `geo:0,0?q=${address}`,
      default: `https://maps.google.com/?q=${address}`,
    });
    Linking.openURL(url ?? `https://maps.google.com/?q=${address}`);
  }, []);

  const sheetTranslateY = sheetAnim.interpolate({
    inputRange: [0, 1],
    outputRange: [SHEET_HEIGHT + 40, 0],
  });

  const quality = selectedProvider?.ccn
    ? qualitySummaries[selectedProvider.ccn]
    : undefined;
  const pinColorSelected = selectedProvider
    ? getPinColor(quality, selectedProvider.rating)
    : "#8F9AB8";

  return (
    <View style={styles.container}>
      <MapView
        ref={mapRef}
        style={styles.map}
        provider={PROVIDER_GOOGLE}
        initialRegion={initialRegion}
        customMapStyle={DARK_MAP_STYLE}
        showsMyLocationButton={false}
        toolbarEnabled={false}
        onPress={hideSheet}
        onMapReady={() => {
          if (providersWithCoords.length > 0) {
            mapRef.current?.animateToRegion(initialRegion, 600);
          }
        }}
      >
        {providersWithCoords.map((provider) => {
          const qs = provider.ccn ? qualitySummaries[provider.ccn] : undefined;
          const pinColor = getPinColor(qs, provider.rating);
          const isSelected = selectedProvider?.id === provider.id;
          return (
            <Marker
              key={provider.id}
              coordinate={{ latitude: provider.latitude, longitude: provider.longitude }}
              onPress={() => showSheet(provider)}
              anchor={{ x: 0.5, y: 1 }}
              zIndex={isSelected ? 10 : 1}
            >
              <View style={[styles.pin, isSelected && styles.pinSelected]}>
                <View style={[styles.pinDot, { backgroundColor: pinColor }]} />
                <View style={[styles.pinTail, { borderTopColor: pinColor }]} />
              </View>
            </Marker>
          );
        })}
      </MapView>

      {providersWithCoords.length === 0 && (
        <View style={styles.noCoords}>
          <Feather name="map" size={22} color={Colors.textMuted} />
          <Text style={styles.noCoordsText}>
            No mappable providers found. Try searching by ZIP code for more precise locations.
          </Text>
        </View>
      )}

      <View style={styles.legend}>
        <LegendDot color="#34C759" label="High quality" />
        <LegendDot color="#D59A32" label="Mid quality" />
        <LegendDot color="#8F9AB8" label="No rating" />
      </View>

      {selectedProvider && (
        <Animated.View
          style={[styles.sheet, { transform: [{ translateY: sheetTranslateY }] }]}
        >
          <View style={styles.sheetHandle} />
          <View style={styles.sheetHeader}>
            <View style={[styles.qualityDot, { backgroundColor: pinColorSelected }]} />
            <Text style={styles.sheetName} numberOfLines={2}>
              {selectedProvider.name}
            </Text>
            <Pressable onPress={hideSheet} hitSlop={10} style={styles.sheetClose}>
              <Feather name="x" size={18} color={Colors.textMuted} />
            </Pressable>
          </View>

          <View style={styles.sheetMeta}>
            <Feather name="map-pin" size={13} color={Colors.textMuted} />
            <Text style={styles.sheetMetaText}>
              {selectedProvider.city}, {selectedProvider.state}
              {selectedProvider.distance ? `  ·  ${selectedProvider.distance} mi` : ""}
            </Text>
          </View>

          {quality && (quality.hciScore || quality.starRating) && (
            <View style={styles.sheetBadges}>
              {quality.starRating && (
                <View style={styles.badge}>
                  <Feather name="star" size={11} color="#D4A017" />
                  <Text style={styles.badgeText}>{quality.starRating} Stars</Text>
                </View>
              )}
              {quality.hciScore && (
                <View style={styles.badge}>
                  <Feather name="bar-chart-2" size={11} color="#1A6DAA" />
                  <Text style={styles.badgeText}>HCI: {quality.hciScore}/10</Text>
                </View>
              )}
            </View>
          )}

          <View style={styles.sheetActions}>
            <Pressable
              style={({ pressed }) => [styles.btnDirections, pressed && { opacity: 0.7 }]}
              onPress={() => handleDirections(selectedProvider)}
            >
              <Feather name="navigation" size={14} color={Colors.primary} />
              <Text style={styles.btnDirectionsText}>Directions</Text>
            </Pressable>
            <Pressable
              style={({ pressed }) => [styles.btnDetails, pressed && { opacity: 0.85 }]}
              onPress={() => {
                hideSheet();
                router.push({
                  pathname: "/provider/[id]",
                  params: { id: selectedProvider.id },
                });
              }}
            >
              <Text style={styles.btnDetailsText}>View Details</Text>
              <Feather name="chevron-right" size={14} color="#FFFFFF" />
            </Pressable>
          </View>
        </Animated.View>
      )}
    </View>
  );
}

function LegendDot({ color, label }: { color: string; label: string }) {
  return (
    <View style={styles.legendItem}>
      <View style={[styles.legendDot, { backgroundColor: color }]} />
      <Text style={styles.legendLabel}>{label}</Text>
    </View>
  );
}

